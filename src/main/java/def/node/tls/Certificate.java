package def.node.tls;
@jsweet.lang.Interface
public abstract class Certificate extends def.js.Object {
    /**
         * Country code.
         */
    public String C;
    /**
         * Street.
         */
    public String ST;
    /**
         * Locality.
         */
    public String L;
    /**
         * Organization.
         */
    public String O;
    /**
         * Organizational unit.
         */
    public String OU;
    /**
         * Common name.
         */
    public String CN;
}

